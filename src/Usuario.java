
public class Usuario {
    
    String ID;
    String nombre;
    int modoViaje;
    boolean accesibilidad;
    


public Usuario(String ID, String nombre, int modoViaje, boolean accesibilidad){         // Constructor de la clase

    this.ID = ID;
    this.nombre = nombre;
    this.modoViaje = modoViaje;
    this.accesibilidad = accesibilidad;
    
}
}